number1 = int(input("Digite um número: "))
number2 = int(input("Digite um segundo número: "))
print("A soma é {} a subtração é {}, a multiplicação é {}, a divisão é {}".format(number1 + number2, number1 - number2, number1 * number2, number1 / number2))
print("A divisão inteira é {}, o resto da divisão é {}, a potenciação é {}".format(number1 // number2, number1 % number2, number1 ** number2))
