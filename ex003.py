#Ler dois números e mostrar a soma deles

num1 = int(input('digite o primeiro número: ')) #variavel do primeiro número
num2 = int(input('digite o segundo número: ')) #variavel do segundo número
soma = num1 + num2 #opreção da soma entre os dois números

print('A soma entre {} e {} é {}'.format(num1, num2, soma)) #Ler o resultado dos dois números somados
